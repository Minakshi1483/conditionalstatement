import java.util.Scanner;
public class uppertolower {


    public static void main(String[] args) {
        char ch;
        int temp;
        //printing uppercase alphabet
        System.out.println("Enter a character in upper case: =");
        for(ch='A' ; ch<='Z'; ch++);
        System.out.println(ch + " ");
        //printing new line
        System.out.println(" ");

        //printing lowercase alphabet
        System.out.println("Lower alphabet:");
        for(ch='a';ch<='z';ch++);
        System.out.print(ch+" ");





    }

}